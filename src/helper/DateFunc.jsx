export const formatDate = (dateObj) => {
    var d = dateObj;
    var month = '' + (d.getMonth() + 1);
    var day = '' + d.getDate();
    var year = d.getFullYear();

    if (month.length < 2) 
        month = '0' + month;
    if (day.length < 2) 
        day = '0' + day;

    return [year, month, day].join('-');
}

export const todayFormatDate = () => {
    return formatDate(new Date())
}